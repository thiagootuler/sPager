    public void atualizaTabelaSituacoes(){
        listSituacoes.clear();
        try {
            for(Pessoa pessoa: DAOPessoa.getLista())
                if(!pessoa.equals(this.pessoa))
                    listSituacoes.add(pessoa.getSituacao());
        } catch (AcessoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        int linha = listSituacoes.size()-1;
        if(linha >=0){
            tblSituacoes.setRowSelectionInterval(linha, linha);
            tblSituacoes.scrollRectToVisible(
                    tblSituacoes.getCellRect(linha, linha, true)
            );
        }
    }

