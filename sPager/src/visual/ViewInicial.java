    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        //JOptionPane.showMessageDialog(this, "Entrou");
        String usuario = this.txtUsuarioL.getText();
        //String msg = "Bem vindo "+usuario;
        //JOptionPane.showMessageDialog(this, msg);
        String senha = this.pwSenhaL.getText();
        try {
            if(!usuario.equals("") && !senha.equals("")){
                Pessoa pessoa = DAOPessoa.procurar(usuario);
                if(pessoa != null){
                    if(pessoa.getSenha().equals(senha)){
                        this.dispose();
                        ViewPrincipal viewPrincipal = new ViewPrincipal(pessoa);
                        viewPrincipal.setVisible(true);
                    }else{    
                        JOptionPane.showMessageDialog(this, "Senha incorreta!", "Erro", JOptionPane.ERROR_MESSAGE);
                        this.pwSenhaL.requestFocus();
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Usu√°rio n√£o encontrado!", "Erro", JOptionPane.ERROR_MESSAGE);
                    this.txtUsuarioL.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Campo n√£o informado!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (AcessoException ex) {
            JOptionPane.showMessageDialog(this, ex, "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = this.txtNome.getText();
        String email = this.txtEmail.getText();
        String usuario = this.txtUsuarioC.getText();
        String senha = this.pwSenhaC.getText();
        try {
            DAOPessoa.cadastrar(new Pessoa(nome, email, usuario, senha));
            //System.out.println(pessoa.toString());
            JOptionPane.showMessageDialog(this, "Usu√°rio cadastrado com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.txtNome.setText("");
            this.txtEmail.setText("");
            this.txtUsuarioC.setText("");
            this.pwSenhaC.setText("");
        } catch (ManutencaoException | AcessoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

