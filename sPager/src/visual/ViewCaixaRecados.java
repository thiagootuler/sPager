    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        int linha = tblRecados.getSelectedRow();
        if(linha+1 <= (tblRecados.getRowCount()-1))
            linha++;
        tblRecados.setRowSelectionInterval(linha, linha);
            tblRecados.scrollRectToVisible(
                tblRecados.getCellRect(linha, 0, true)
            );
    }//GEN-LAST:event_btnProximoActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        int linha = tblRecados.getSelectedRow();
        if(linha-1 >= 0)
            linha--;
        tblRecados.setRowSelectionInterval(linha, linha);
            tblRecados.scrollRectToVisible(
                tblRecados.getCellRect(linha, 0, true)
            );
    }//GEN-LAST:event_btnAnteriorActionPerformed

    private void atualizaTabelaRecados(){
        listRecados.clear();
        try {
            for(Recado recado: DAORecado.getLista())
                if(recado.getDestinatario().equals(this.pessoa)){
                    listRecados.add(recado);
                }
        } catch (AcessoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
        int linha = listRecados.size()-1;
        if(linha >= 0){
            tblRecados.setRowSelectionInterval(linha, linha);
            tblRecados.scrollRectToVisible(
                    tblRecados.getCellRect(linha, linha, true)
            );
        }else{
            this.btnAnterior.setEnabled(false);
            this.btnProximo.setEnabled(false);
            this.btnResponder.setEnabled(false);
        }
    }

